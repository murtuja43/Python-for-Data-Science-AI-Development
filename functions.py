def calculate_total(a, b):  # Parameters: a and b
    total = a**2 + b**2           # Task: Addition
    return total            # Output: Sum of a and b

result = calculate_total(5, 7)  # Calling the function with inputs 5 and 7
print(result)  # Output: 12


